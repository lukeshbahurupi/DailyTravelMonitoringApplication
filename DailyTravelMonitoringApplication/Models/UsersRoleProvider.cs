        public override string[] GetRolesForUser(string username)
        {
            using (TravelingTeam_DB_Context context = new TravelingTeam_DB_Context())
            {
               var UserRoles = (from user in context.Users join roleMapping in context.UserRolesMappings on user.Id equals roleMapping.UserID
               join role in context.RoleMasters on roleMapping.RoleID equals role.ID where user.MobileNo == username select role.RollName).ToArray();
                return UserRoles;
            }
